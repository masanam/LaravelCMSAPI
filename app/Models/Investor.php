class Investor extends Model
{
    use SoftDeletes;

    public $table = 'investors';
    

    protected $dates = ['deleted_at'];


    public $fillable = [
        'name',
        'email',
        'phone',
        'country',
        'title',
        'content',
        'status',
        'created_by',
        'updated_by'
    ];

    /**
     * The attributes that should be casted to native types.
     *
