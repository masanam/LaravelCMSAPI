class Certification extends Model
{
    use SoftDeletes;

    public $table = 'certifications';
    

    protected $dates = ['deleted_at'];


    public $fillable = [
        'issuer',
        'title',
        'seotitle',
        'content',
        'picture',
        'year',
        'status',
        'created_by',
        'updated_by'
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
